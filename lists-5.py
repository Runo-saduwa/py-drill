
# list of numbers
my_list = [1, 2.4, 49, 0]
print(my_list)

# list of stigns
people_list = ['runo', 'tega', 'ejiro', 'gare']
print(people_list)

# accessing the last item in a list
print(people_list[-1])

# modify list
my_list[0] = 200
print(my_list)

# length of a list
print(len(my_list))

# slice list
print(my_list[0:1])


# add to the end of the list
my_list.append(1000)
print(my_list)

# insert to a position
my_list.insert(2, 50)
print(my_list)

# remove a value
my_list.remove(50)

#  remove  element from index
my_list.pop(0)

# sort from least to greatest
my_list.sort()